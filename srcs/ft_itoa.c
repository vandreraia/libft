#include <stdlib.h>

char *ft_itoa(int n)
{
	int i;
	int temp;
	char *num;

	i = 0;
	temp = n;
	while (temp > 0)
	{
		temp /= 10;
		i++;
	}
	num = (char *)malloc(sizeof(char) * i + 1);
	num[i--] = '\0';
	while (n > 0)
	{
		num[i] = n % 10 + '0';
		n /= 10;
		i--;
	}
}
